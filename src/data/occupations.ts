export function getOccupationBySlug(slug: string): Occupation | undefined {
  return occupations.find((o) => o.slug === slug);
}

export function getTopAtRisk(n = 5): Occupation[] {
  return [...occupations].sort((a, b) => b.dri - a.dri).slice(0, n);
}

export function getTopResilient(n = 5): Occupation[] {
  return [...occupations].sort((a, b) => a.dri - b.dri).slice(0, n);
}

export function getOccupationsByIndustry(industryId: string): Occupation[] {
  return occupations.filter((o) => o.industry === industryId);
}

export function getDriColor(dri: number): string {
  if (dri >= 80) return "#EF4444";
  if (dri >= 60) return "#F97316";
  if (dri >= 40) return "#EAB308";
  return "#22C55E";
}

export function getDriLabel(dri: number): string {
  if (dri >= 80) return "Critical";
  if (dri >= 60) return "High";
  if (dri >= 40) return "Moderate";
  return "Low";
}

export function getDriGrade(dri: number): string {
  if (dri >= 90) return "F";
  if (dri >= 80) return "D";
  if (dri >= 70) return "D+";
  if (dri >= 60) return "C";
  if (dri >= 50) return "C+";
  if (dri >= 40) return "B";
  if (dri >= 30) return "B+";
  if (dri >= 20) return "A";
  return "A+";
}